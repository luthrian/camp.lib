package com.camsolute.code.camp.lib.contract;

/**
 * Objects that honar this contract provide the order business id value when requested and allow
 * it to be updated. The order business id is the current business id of a business order.
 * Objects must reference the business order object instance to ensure that the id value they
 * provide is current.
 *
 * @author Christopher Campbell
 */
public interface HasOrderBusinessId {
  public String orderBusinessId();
  
  public void setBusinessId(String businessId);

  public String updateOrderBusinessId(String newOrderBusinessId);

  public String initialOrderBusinessId();

  public String onlyOrderBusinessId();
}
